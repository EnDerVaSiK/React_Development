// (a > 10 ? a : a * 2) > 5
// (a > 10 && a > 5) эквивалентно (a > 10)
// (a < 10 && a * 2 > 5) эквивалентно (a < 10 && a * 2 > 5)
// значит ((a > 10 ? a : a * 2) > 5) эквивалентно (a * 2 > 5)
// получим:
// (a * 2 > 5) ? (2 * a) + 1 : (a < 3 ? 1 : 2 * (a - 2)) > 4 ? 5 : (a % 2 == 0 ? 6 : 7)

// ((a < 3 ? 1 : 2 * (a - 2)) > 4)
// (a < 3 && 1 > 4) эквивалентно (false)
// (2 * (a - 2) > 4) эквивалентно (a > 4)
// значит ((a < 3 ? 1 : 2 * (a - 2)) > 4) эквивалентно (a > 4)
// получим:
// (a * 2 > 5) ? (2 * a) + 1 : (a > 4) ? 5 : (a % 2 == 0 ? 6 : 7)

// вывод "5" никогда не достигнем, так как:
