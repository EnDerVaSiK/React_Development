// поэтому получим:
// (a * 2 > 5) ? (2 * a) + 1 : (a % 2 == 0 ? 6 : 7)


console.log("Мое решение Tern1 с if...else:");

if (a * 2 > 5) {
    console.log((2 * a) + 1);
} else if (a % 2 == 0) {
    console.log(6);
} else {
    console.log(7);
}


console.log("Мое решение Tern1 с switch():");

switch (true) {
    case a * 2 > 5:
        console.log((2 * a) + 1);
        break;
    case a % 2 == 0:
        console.log(6);
        break;
    case !(a % 2 == 0):
        console.log(7);
        break;
}
